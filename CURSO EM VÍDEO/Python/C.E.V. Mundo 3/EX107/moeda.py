def aumentar(numero=0, porcentagem=10):
    res = numero + numero * (porcentagem/100)
    return res


def diminuir(numero=0, porcentagem=13):
    res = numero + numero * (porcentagem/100)
    return res


def dobro(numero):
    res = numero * 2
    return res


def metade(numero):
    res = numero / 2
    return res
