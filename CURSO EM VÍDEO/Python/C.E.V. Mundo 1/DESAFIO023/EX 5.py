num = int(input('Informe Numero: '))
und = num // 1 % 10
dez = num // 10 % 10
cen = num // 100 % 10
mil = num // 1000 % 10

print('Analisando o numero {}...'.format(num))
print('Unidade: {}'.format(und))
print('Dezena: {}'.format(dez))
print('Centena: {}'.format(cen))
print('Milhar: {}'.format(mil))

